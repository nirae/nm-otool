#include "nm-otool.h"

uint32_t	uint32_swap(uint32_t nb)
{
	nb = ((nb << 8) & 0xFF00FF00) | ((nb >> 8) & 0xFF00FF);
	return ((nb << 16) | (nb >> 16));
}
